package com.zopa.borrowercalc.model.entities;

import java.math.BigDecimal;

public class LenderOffer implements Cloneable {

	private String name;

	private BigDecimal rate;

	private Integer available;

	public LenderOffer() {

	}

	public LenderOffer(String name, BigDecimal rate, Integer available) {
		this.name = name;
		this.rate = rate;
		this.available = available;
	}

	public String getName() {
		return name;
	}

	public void setName(String name) {
		this.name = name;
	}

	public BigDecimal getRate() {
		return rate;
	}

	public void setRate(BigDecimal rate) {
		this.rate = rate;
	}

	public Integer getAvailable() {
		return available;
	}

	public void setAvailable(Integer available) {
		this.available = available;
	}

	@Override
	public int hashCode() {
		final int prime = 31;
		int result = 1;
		result = prime * result + ((available == null) ? 0 : available.hashCode());
		result = prime * result + ((name == null) ? 0 : name.hashCode());
		result = prime * result + ((rate == null) ? 0 : rate.hashCode());
		return result;
	}

	@Override
	public boolean equals(Object obj) {
		if (this == obj)
			return true;
		if (obj == null)
			return false;
		if (getClass() != obj.getClass())
			return false;
		LenderOffer other = (LenderOffer) obj;
		if (available == null) {
			if (other.available != null)
				return false;
		} else if (!available.equals(other.available))
			return false;
		if (name == null) {
			if (other.name != null)
				return false;
		} else if (!name.equals(other.name))
			return false;
		if (rate == null) {
			if (other.rate != null)
				return false;
		} else if (!rate.equals(other.rate))
			return false;
		return true;
	}

	@Override
	public String toString() {
		return "LenderOffer [name=" + name + ", rate=" + rate + ", available=" + available + "]";
	}

	@Override
	public LenderOffer clone() throws CloneNotSupportedException {
		return (LenderOffer) super.clone();
	}

}
